public class Cliente {


    String nome;
    String sobrenome;
    int idade;
    String numeroconta;
    String numeroagencia;
    int codigobanco;

    public String getNome() {
        return nome;
    }

    public void setNome(String nome) {
        this.nome = nome;
    }

    public String getSobrenome() {
        return sobrenome;
    }

    public void setSobrenome(String sobrenome) {
        this.sobrenome = sobrenome;
    }

    public int getIdade() {
        return idade;
    }

    public void setIdade(int idade) {
        this.idade = idade;
    }

    public String getNumeroconta() { return numeroconta; }

    public void setNumeroconta(String numeroconta) {this.numeroconta = numeroconta;}

    public String getNumeroagencia() {return numeroagencia;}

    public void setNumeroagencia(String numeroagencia){this.numeroagencia = numeroagencia;}

    public int getCodigobanco() {return codigobanco;}

    public void setCodigobanco(int codigobanco){this.codigobanco = codigobanco;}

}

