public class ClienteVend {

    String nomeCliente;
    String codGrupo;
    int codCota;
    String cpf;
    int salario;
    int valorParcela;

    public String getNomeCliente() { return nomeCliente; }

    public void setNomeCliente(String nomeCliente) { this.nomeCliente = nomeCliente; }

    public String getCodGrupo() { return codGrupo; }

    public void setCodGrupo(String codGrupo) { this.codGrupo = codGrupo; }

    public int getCodCota() { return codCota; }

    public void setCodCota(int codCota) { this.codCota = codCota; }

    public String getCpf() { return cpf; }

    public void setCpf(String cpf) { this.cpf = cpf; }

    public int getSalario() { return salario; }

    public void setSalario(int salario) { this.salario = salario; }

    public int getValorParcela() { return valorParcela; }

    public void setValorParcela(int valorParcela) { this.valorParcela = valorParcela; }



}
